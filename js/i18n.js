// ========================================
// Language Engine
// ========================================
function getCurrentLang() {
  return localStorage.getItem('pmp_lang') || 'id';
}

function setLang(lang) {
  localStorage.setItem('pmp_lang', lang);
  document.documentElement.setAttribute('lang', lang);
  document.documentElement.setAttribute('data-lang', lang);
  applyLanguage(lang);
  updateLangToggle(lang);
  // Notify page-specific scripts to re-render dynamic content
  window.dispatchEvent(new CustomEvent('langChange', { detail: { lang: lang } }));
}

function applyLanguage(lang) {
  const dict = translations[lang] || translations['id'];

  // Text content
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (dict[key] !== undefined) {
      el.textContent = dict[key];
    }
  });

  // HTML content (for rich text)
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const key = el.getAttribute('data-i18n-html');
    if (dict[key] !== undefined) {
      el.innerHTML = dict[key];
    }
  });

  // Placeholder
  document.querySelectorAll('[data-i18n-ph]').forEach(el => {
    const key = el.getAttribute('data-i18n-ph');
    if (dict[key] !== undefined) {
      el.placeholder = dict[key];
    }
  });
}

function updateLangToggle(lang) {
  const btn = document.getElementById('lang-toggle');
  if (btn) {
    if (lang === 'id') {
      btn.innerHTML = '<span class="lang-active">ID</span> | EN';
    } else {
      btn.innerHTML = 'ID | <span class="lang-active">EN</span>';
    }
  }
}

function toggleLanguage() {
  const current = getCurrentLang();
  const next = current === 'id' ? 'en' : 'id';
  setLang(next);
}

// Expose globally
window.toggleLanguage = toggleLanguage;
window.getCurrentLang = getCurrentLang;
window.translations = translations;

// Auto-init on DOM ready
document.addEventListener('DOMContentLoaded', () => {
  const lang = getCurrentLang();
  document.documentElement.setAttribute('lang', lang);
  document.documentElement.setAttribute('data-lang', lang);
  applyLanguage(lang);
  updateLangToggle(lang);
});
